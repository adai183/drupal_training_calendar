                        <p>
                            Cellular processes are carried out by <strong>macromolecules</strong>, mainly proteins and nucleic acids, which work as true <strong>nanoscopic machines</strong> that act in isolation or, more commonly, associated in <strong>macromolecular complexes</strong>. The dynamic nature of these complexes makes their study difficult, but learning how they work is critical to understand cellular function and the basis of certain diseases.
                        </p>
                        <p>
                            In our group we combine <strong>X-ray crystallography</strong> and <strong>electron microscopy</strong> to study the three-dimensional structure of macromolecules and their complexes. Structural studies are complemented with other biochemical and biophysical techniques. These studies help us understand how nanoscale machines perform important cellular processes, as well as open avenues for the development of drugs against various diseases.
                        </p>
                        <h3 style="color:#333333;">MAIN RESEARCH LINES</h3>
                        <p>
                            <span style="color:#333333;">1. Eukaryotic transcription</span><br>
                            Eukaryotic cells have three RNA polymerases (Pol) to transcribe their genetic content. Pol I synthesizes ribosomal RNA, Pol II produces all messenger RNA, and Pol III is responsible for transfer RNA. Coordination of these three enzymes is essential to maintain cellular homeostasis.<br>
                            <strong>Pol I</strong> is a complex of 14 proteins with a total mass of 600 kDa, responsible for synthesizing the core of the ribosome. Regulation defects in Pol I transcription associate with problems in cell proliferation and, hence, with tumor development. We have determined the structure of this enzyme to 3 Å resolution using X-ray crystallography. The atomic structure explains fundamental properties of the enzyme, such as the incorporation of functional modules that must be recruited in other Pols, while suggesting a unique activation mechanism. These results were recently published in <a href="http://www.nature.com/nature/journal/v502/n7473/full/nature12636.html">Nature</a> and appeared in <a href="http://www.huffingtonpost.es/carlos-fernandez-tornero/viaje-al-centro-de-la-celula_b_4178263.html">national</a> and <a href="https://nature.altmetric.com/details/1853759">international</a> media.
                        </p>
                        <img src="img/pol1.png" alt="" class="img-responsive" />
                        <p>
                            <strong>Pol III</strong>, consisting of 17 subunits with a total mass of 700 kDa, transcribes several small RNA genes such as transfer RNA. We have determined the structure of this macromolecular complex to 10 Å resolution using electron cryomicroscopy, both in the presence and absence of nucleic acids. These studies, in combination with native mass spectrometry experiments, have enabled us to understand the architecture and organization of this essential enzyme. These results were published in <a href="http://emboj.embopress.org/content/29/22/3762">The EMBO</a> Journal and RNA Biology.
                        </p>
                        <img src="img/pol3.png" alt="" class="img-responsive" style="margin: 0 auto;"/>
                        <p>
                            <span style="color:#333333;">2. Clinical applications against angiogenic diseases.</span><br>
                             Guillermo Giménez-Gallego was a member of the team that described the first angiogenic growth factor (FGF). Later he spent considerable effort in discovering inhibitors of FGF and at the end of 2010 he described a family of potent inhibitors derived of gentisic acid (GA). Meanwhile it was discovered that FGF induces angiogenesis as it triggers inflammation. The inhibitors of the GA family have shown, in the clinic, a remarkable capacity of controlling some inflammations, such as those causing macular degeneration.
                        </p>
