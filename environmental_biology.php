                        Environmental Biology is a Research Line created at the CIB with the aim of increasing our knowledge on the biological processes that are implicated in the interactions between the natural environment and some human activities involving plants and/or microorganisms. The idea is to develop more eco-efficient processes with much lower environmental impacts in frame with a sustainable bioeconomy, and thus contributing to circumvent the effects of climate change. The Line includes an interdisciplinary group of scientists interested in studying at both, basic and applied levels, the relationships among the environment and plants and microorganisms, not only for understanding the basic subjacent biological mechanisms, but also for mitigating problems, which directly or indirectly affect environment sustainability, rised in agriculture and industrial production. It combines the expertise of participating researchers on different established and novel approaches that are essential to cope with current and future environmental changes. The Environmental Biology Line has evolved from our original strengths in different disciplines to focus on two main areas of interest that constitute two research sublines: Microbiology for a Sustainable Development and Plant-Environment Interactions. Elucidating the biochemical and genetic principles in environmental relevant microorganisms will provide the know-how to counteract the negative impacts of pollutants and to design cleaner chemical processes. On the other hand, understanding the interaction between plants and their biotic and abiotic environment is essential to comprehend the effects of global change and their impact on biodiversity and ecosystem functioning, and for development of strategies for a sustainable agriculture.
