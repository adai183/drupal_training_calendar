                <div class="col-md-8 col-md-offset-1 margin-top-2">
                    An international study with the participation of Prof.
                    Guillermo Giménez Gallego and the NMR group at the Centro
                    de Investigaciones Biológicas (CSIC), has been just
                    published in Cell Reports. This study contains the
                    discovery and determination of the atomic structure,
                    by NMR spectroscopy, of the protein EPI-X4.
                    This protein prevents that the CXCL12 cytokine interacts
                    with the protein CXCR4, its receptor in the cellular
                    membrane. The knowledge of atomic structure of EPI‑X4 has
                    allowed designing analogues which are one hundred times
                    more potent and can serve as therapeutic agents to treat
                    very important diseases, since the high levels of CXCL12
                    are associated to inflammations which are difficult
                    to treat, as well as to tumor metastasis.
                </div>
