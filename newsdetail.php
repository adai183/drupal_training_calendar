                    MRNA DEGRADATION
                </div>
                <div class="col-md-8 col-md-offset-4  margin-top-2">
                    On the other hand, EPI-X4 and the analogues described in
                    the paper inhibit the infection of the X4VIH-1 virus, which
                    is the variety predominant in HIV patients from the moment
                    they start to show symptoms, because the virus requires to
                    bind CXCR4 to invade the cell. Moreover, EPI‑X4 is present
                    abundantly in the urine of patients which suffer from
                    inflammatory kidney diseases, which might allow its use as
                    a marker to diagnose these diseases.
                    The atomic structure of EPI‑X4 adopts a ring-type
                    disposition with a “tail”. The ring shows a strongly
                    hydrophobic surface as well as one with high density
                    of positive charges, capable to interact with high affinity
                    with the extracellular region of CXCR4, which
                    is extraordinarily abundant in negative charges.
                </div>

