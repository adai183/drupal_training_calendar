                        <p>
                            Our lab uses cellular and animal models to understand the <strong>physiological roles of autophagy</strong> and its implications during <strong>disease</strong>. Autophagy is an essential intracelullar degradation pathway that recycles cell components generating new building blocks and energy to maintain cellular homeostasis. Autophagy plays an important role in the response to nutrient starvation; the recycling of damaged organelles and is a survival mechanism under stress conditions. In addition, autophagy could as well participate in programmed cell death.
                        </p>
                        <p>
                            We are interested in the implication of autophagy during <strong>development</strong> and in the relationship of autophagy with basic processes such as proliferation, differentiation and cell death. We have demonstrated that autophagy is essential for eliminating apoptotic cells during cell death associated to retinal neurogenesis. We are currently studying retinal development in knockout animals of several autophagy regulators.
                        </p>
                        <p>
                            Moreover we want to understand how autophagy deregulation may play a role in several pathological situations such as <strong>cancer</strong> and <strong>neurodegenerative conditions</strong>. We have demonstrated that autophagy plays a cytoprotective role in animal models of Parkinson Disease and in models of axonal damage. In addition. autophagy is essential for antitumoral action of several compounds. In addition we are interested in the search of new therapies targeting autophagy. We have set up the metodology for screening by flow cytometry the ability of compounds to activate/downregulate autophagy. We have several projects with pharmaceutical companies to screen for new drugs that modulate autophagy with the aim to find <strong>new treatments</strong> for cancer, neurodegenerative diseases and other pathological conditions.
                        </p>
