                        <p>
                            Plants are sessile and need to cope with adverse environmental conditions that affect their development. To this end, plants have evolved adaptive processes that enable them to survive stresses such as drought, freezing or salinity. The work in our laboratory is aimed to elucidate the molecular mechanisms underlying these processes, which ultimately will allow improving crop productivity and sustainability in agriculture and environment. Our research program largely focuses in the molecular genetics of low temperature sensing and signaling. The goal is to identify and characterize key components of the signal transduction pathways that mediate cold acclimation, an adaptive process whereby many plants increase their freezing tolerance in response to low, nonfreezing temperatures. As many plant responses to abiotic stresses are largely conserved, the identification of signaling components involved in cold acclimation also provides insights into the mechanisms controlling plant tolerance to other important adverse environmental conditions such as drought and soil salinity. Most of our work is carried out with Arabidopsis, a model plant that is able to cold acclimate. Using a combination of genetic, cell biology, molecular and biochemical approaches, we have identified transcriptional networks and gene modules that regulate cold acclimation and confer tolerance to freezing and other related stresses. Interestingly, some of these gene modules resulted to be constituted by novel regulatory elements related to post-transcriptional control of gene expression, including RNA-binding proteins and long-noncoding RNAs. Current efforts are mainly dedicated to understand the mechanisms of action of these elements and their roles in abiotic stress responses. The conservation of the transcriptional networks and gene modules identified in Arabidopsis in important crops such as tomato is also being subject of study.
