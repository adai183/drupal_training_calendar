                        <p>
                            The Translational medicinal and biological chemistry group is focused on the design, synthesis, biological evaluation, study and further optimization of pharmacokinetic properties of structurally diverse chemical entities for drug discovery. The efforts of our group are focused on the pharmacological validation of new targets for neurodegenerative diseases which will allow the discovery of innovative drugs with novel mechanism of action as disease-modifying agents for unmet severe pathologies. In order to do this, classical medicinal chemistry methodologies and techniques will be applied.
                        </p>
                        <p>
                            For the design of our drugs (mainly small heterocyclic molecules with MW<500), different strategies will be used. These include computer-aided drug design, multifunctional compounds (DML´s) bearing different pharmacophore moieties in the same molecule to interact with different targets, and improving ADME properties of the candidates, among others. Our own chemical library containing hundreds of compounds with privileged scaffolds will be also used in directed biological screening programs.
                        </p>
                        <p>
                            Our research is applied with a high content of translational research. The group´s research programs are designed from the early stages of drug discovery to proof of efficacy in representative animal models. Scientific collaborations and technology transfer to companies in the pharmaceutical sector are key in achieving our goals.
