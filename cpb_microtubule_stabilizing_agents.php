                        <p>
                            One of the most important targets for chemotherapy is the mitotic spindle, mainly composed of tubulin. Its function is dependent on microtubule dynamics which result from stochastic binding and dissociation of αβ–tubulin dimers to/from the microtubule ends. Microtubule growth or length reduction depends on the activation state of tubulin, which is controlled by the nucleotide bound to β subunit. GTP-tubulin is the biologically activated form and binds to the microtubule ends causing its growth, while GDP-tubulin dissociates from the ends, making them shorter. These elongation and reduction events constitute the microtubule dynamics whose fine regulation is needed for chromosome separation and therefore, for cell division.
                        </p>
                        <p>
                            There are several compounds capable of binding to tubulin, modulate its activation state (either activating or deactivating tubulin) and thus interfere with the microtubule dynamics, stopping cells in mitosis and inducing apoptosis, some of them, vinblastine, paclitaxel, docetaxel are widely used in pharmacological treatment of neoplasic diseases. However although there are a growing number of compounds active against tumours targeting different cellular functions necessary for cell division as targets, chemotherapy has still unsatisfactory clinical results. Although 70% of the metastatic breast cancers respond initially to docetaxel treatment, in only 5% of them there is a complete remission. This is in approximately 50% of the cases due to the appearance of the so called multiple drug resistance MDR, which describes the capability of tumours in contact with a certain cytotoxic drug to develop resistance to a wide range of antitumour drugs. Although there could be several mechanisms involved in these phenomena, the main one is the decrease in intracellular concentration of the drug, due to the overexpression of membrane pumps, in particular P-glycoprotein.
                        </p>
                        <p>
                            This research line aims to understand the biochemical and biological mechanisms that tubulin modulators use to regulate the activation state of tubulin in order to deepen our knowledge of how to design tubulin modulators with better biochemical and biological properties and specially able to overcome MDR.
                        </p>
                        <p>
                            To address this problem we work in collaboration with a network of organic chemistry and natural products groups with the three following objectives: Identify new target sites in tubulin, find new lead compounds for the existing target sites, and optimice the biochemical and biological properties of the know compounds. In order to do so we study the mechanism of action on tubulin of a large number of compounds employing both biophysical and biochemical techniques and evaluating their efect in tumoural cell lines both resistant and non resistant.
                        </p>
                        <p>
                            The group have obtained important findings in the field in the last years which include:
                        </p>
                        <ul>
                            <li>The laulimalide site, the second binding site of microtubule stabilizing agents and at least two different kind of compounds with  activity in this site. confirming the structural promiscuity  already observed with the paclitaxel site (<a href="http://www.cib.csic.es/repositorio_bd/publicacion/1314/urls_documento/2006_Barbero.pdf">see paper</a>). </li>
                            <li>The finding that affinity is a predictive value for the cytotoxicity of paclitaxel related compounds and the additivity of the free energy contributions of the different substituents of the active molecules to the total binding energy (<a href="http://www.cib.csic.es/repositorio_bd/publicacion/671/urls_enlace/Buey_et_al_ChemBiol._2005.pdf"></a>).</li>
                            <li>The finding that affinity is a predictive value for the cytotoxicity of paclitaxel related compounds and the additivity of the free energy contributions of the different substituents of the active molecules to the total binding energy (<a href="http://www.cib.csic.es/repositorio_bd/publicacion/671/urls_enlace/Buey_et_al_ChemBiol._2005.pdf">see paper</a>).</li>
                            <li>The finding that cyclostreptin is the first microtubule stabilizing agent that binds covalently to microtubules and the fact that the multidrug resistant cells overexpressing p-glycoprotein do not show resistance to these compound. The use of these covalent reagent has allowed the elucidation of the pathway that paclitaxel follows to reach its luminal site in the microtubules and the existence of a new site for  microtubule stabilizing agents in the outer surface of microtubules.</li>
                        </ul>
