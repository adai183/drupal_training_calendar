                        <p>
                            The phospholipid matrix of biological membranes acts not only as a physical scaffold for the rest of membrane components, but also as permeability barrier to restrict traffic of molecules across the different organelles or with the extracellular space in a selective manner. The preservation of the intracellular or intra-organellar milieu is, thus, warranted. Nevertheless, the preservation of the structure of the phospholipid matrix throughout the whole range of living organisms is a severe handicap for its use as pharmacological target. Nowadays only polyenes and some membrane-active peptides were used in anti-infectious chemotherapy. In sharp contrast, one of the most effective ways to tackle incipient invasion by pathogens in Nature, is the formation of a protective chemical barrier made by membrane-active peptides. Furthermore, this membrane interaction and translocation across membranes independent of  their recognition by specific translocator/transporters, is employed by some proteins to involved in transcellular  signal transduction pathways.
                        </p>
                        <p>
                            According to the peptide-membrane interaction we may arbitrarily group them into three major groups:
                            <ul style="list-style: decimal;">
                                <li>Peptides whose microbicidal activity is mostly based on an irreversible permeation of the cell membrane of the target cell. This is achieved through massive peptide insertion into the phospholipid matrix. In general, they are strongly cationic peptides with intrinsic amphipatic structures or adopted after insertion into the pathogen membrane. Their selectivity relies on the  higher content  of anionic phospholipids in the cell membrane of prokaryotes and lower eukaryotes. Most importantly, these phospholipids are exposed to the external medium, as such, amenable for an initial electrostatic interaction with the peptides. This is in contrast with the higher eukaryotes, as acidic phospholipid are confined into the cytoplasmic leaflet of the plasma membrane. Accordingly, membrane-peptide interaction is precluded in these organisms. Two chemotherapeutical advantages for these peptides are their activity on a wide spectrum of pathogens and the low rate of resistance induction. The latter will require dramatic changes in the phospholipid composition of the membrane, with simultaneous effect on the activity of the enzymatic and transport systems located there.</li>
                                <li> Antimicrobial peptides with intracellular targets.- Their interaction with the membrane is reversible and transitory and limited to their translocation into the intracellular space. Membrane damage is usually mild and transitory. Once inside, binding to their respective target is under the same conditions of aa typical antibiotic, in terms of specificity and induction of resistance.</li>
                                <li>Cell penetrating peptides. Their interaction with membranes is similar to the previous group; nonetheless an intrinsic microbicidal activity is not mandatory. Their potentiality in chemotherapy comes from their role as peptide vehicles to transport across the membrane a variety of molecules associated or conjugated to them. This may include biopolymers, nanoparticles, quantum dots…, otherwise impermeable to it. This independence from canonical transporters/translocators broadens the repertoire of therapeutic molecules to peptides, proteins or nucleic acids, unable to penetrate the cells. The inclusion of organelar import sequences to the CPP sequence affords the allocation of the cargo molecule into a specific intracellular location. The increase of the local concentration of the drug near its(their) target(s) decreased the effective dose of the drug, while avoids unwanted side-effect caused by spurious interaction outside the target organelle.</li>
                            </ul>
                        </p>
                        <p>
                             Research in our group is focused on the assay, design and production of membrane-active peptides. These reagents were applied on the chemotherapy on two pathogens, Acinetobacter baumannii, a Gram negative bacteria and the protozoon Leishmania sp. Chemotherapy against both pathogens underwent a rapid decline in efficacy due to resistance and the paucity of drugs available. Our quest is: a) To discover new active leads on these pathogens , preferentially peptides. b) To optimize their activity through SAR studies. c) To elucidate their mechanism of action and targets involved through a variety of biochemical, biophysical and metabolomic techniques. d) To design new administration pathways based on peptide vehicles for new and current clinical drugs. e).-Recombinant production of peptides and proteins with chemotherapeutic value. Just to illustrate these issues, in collaboration with other groups we have defined and characterized  the mechanism of leishmanicidal activity for cecropin A-melittin hybrids peptides, gramicidin S and their analogues, the bacteriocin AS-48, and as epitome for antimicrobial peptides with intracellular targets, histain 5 and the eosinophil cationic protein.
                        </p>
