                            The research of the group “Structural Biology of Proteins” is mainly focused in the study of the structural basis of the biological properties of proteins, with the goal of affording leads for future industrial developments. We characterize both individual proteins and <a href="cpb_structural_assembly.php">macromolecular assemblies</a>. Once a research goal is established the work of the group normally begins with the design and development of systems for production of the targeted protein with the quality required for high resolution studies. Optimization of protein synthesis can be quite adequately addressed with the equipment available in either their laboratories or at the Center (analytical ultracentrifugation, visible/UV transmission and fluorescence spectroscopy, circular dichroism, mass spectrography, high performance exclusion chromatography etc.). The groups are also equipped (or have free access) to typical protein chemistry instruments (protein sequencer, amino acid analyzer, protein synthesizer) and protein purification equipments (medium and high performance liquid chromatography).The group has X-ray diffraction and nuclear magnetic resonance (500 MHz) facilities. It has also regular access to the European synchrotron services and the 600 and 800 MHz spectrometers of the high field spectroscopy equipment of the CSIC. The group is also a regular user of the 750 MHZ spectrometer of the Universidad de Santiago de Compostela (750 MHz).
