                        <p>
                            The stress-induced cell reprogramming towards embryogenesis of the pollen is a powerful biotechnological tool for plant breeding, as the fastest way for rapid generation of new varieties through double haploid plants. The characterization of its regulating <strong>mechanisms</strong> (genetic, epigenetic, molecular, cellular) will permit to exploit the process more efficiently for sustainable agriculture, selection of forest resources and environment control. Also, we are studying new possibilities for the treatment of phytopathologies with nanobiotechnology approaches, by using magnetic nanoparticles and Quantum Dots for diagnosis and treatments with minimum doses.
                        </p>
                        <p>
                            <strong>Stress-induced pollen reprogramming to embryogenesis: biotechnology tool for plant breeding</strong><br>
                            We analyze the mechanisms that control the reprogramming and totipotency of the microspore and embryogenesis initiation through a multidisciplinary and integrative study of the physiology of key events that limit its efficiency and correct progression. We use model herbaceous species and plant of agroforest interest (horticultural, cereals, fruit and forest trees). We characterize the determinant factors of cell totipotency acquisition, reprogramming and embryogenesis initiation, in response to stress, for their use as selective targets in strategies for optimization of the system and application in breeding programs of crops, agronomic and environmental. By means of a multidisciplinary and integrated approach with techniques of in situ molecular identification, correlative microscopy, molecular and cell biology and plant physiology, we studied the following elements and processes, which have a key role in the pollen reprogramming: a) Programmed cell death (PCD) and autophagy, which regulate the balance between cell survival and death during the process; b) epigenetic regulation operated by DNA methylation and histone modifications, activators and repressors of transcriptional activity; c) dynamics of phytohormones (auxins and its balance with cytokinins); d) cell wall remodeling mediated by pectin methyl esterase and arabinogalactan proteins (AGPs).
                        </p>
                        <p>
                            <strong>Nanoparticles (NPs) for diagnosis and treatments in crop protection:</strong><br>
                            We study the mechanisms of internalization and transport in planta of magnetic NPs and Quantum Dots (QDs), as well as their possible cytotoxicity, and the potential uptake of NPs and QDs by the plant. Moreover, we analyze how the dynamics of NPs entrance and movility changes when NPs are functionalized with biomacromolecules that label pathogens (for diagnosis) or with herbicides, fertilizers and pesticids (as controlled delivery systems for plant treatments). We use Medicago, as model, and different crops like pumpkin, pea, tomate, sunflower and wheat.
                        </p>
