                        <p>
                            Microorganisms will be for sure the first living beings on which we will achieve understanding, both as biological systems and on the details of the functional architecture of their macromolecular components. Microbiology is thus now a focal point of convergence for Physics, Chemistry, Nanosciences and Mathematical modelling.
                        </p>
                        <!--img src="img/syn_mic.png" alt="" class="img-responsive" /-->
                        <p>
                             Our group approaches, from multidisciplinary perspectives, objectives belonging to the emergent field of <strong>Synthetic Biology</strong>: the generation in microorganisms of non-natural models of <strong>amyloid proteinopathies</strong>, as useful shortcuts to neurodegenerative diseases. The aim is to later deconstruct their underlying complex core mechanisms. With such purpose, we started from the knowledge acquired (see research line 1) on the conformational changes experienced by ´winged-helix´ (WH) domains upon binding to DNA, as a mechanism enabling initiation of DNA replication of bacterial plasmids (RepA protein), or yeast chromosomes (ORC). RepA protein, upon origin firing, is driven to aggregation through its WH1 domain, constituting a mechanism for negative control of DNA replication.
                        </p>
                        <p>
                            Through engineering the molecular determinants of RepA-WH1 aggregation (see research line 2), we have built an amyloidogenic device in which fibril assembly in vitro is promoted by interaction with DNA. In the bacterium Escherichia coli, RepA-WH1 generates a vertically transmissible (from mother to daughter cells) amyloid proteinopathy, modulated by an Hsp70 chaperone.  RepA-WH1 is bio-safe, since it is not infectious (i.e., not horizontally transmissible) and there is no human protein with a significant degree of sequence similarity. Starting from this synthetic device, we are interested, as biotechnological goals, in the development of new tools for intervening on protein amyloidosis, such as sensors and inhibitor molecules.
                        </p>
