                        <div class="col-md-9">
                            <p>
                                Analytical ultracentrifugation is an important group
                                of powerful methods (sedimentation equilibrium and velocity)
                                which allow determine the size, the approximate
                                overall shape and the degree of homogeneity of
                                proteins and other biological macromolecules in
                                solution. These methods are specially adapted for
                                the detection and quantitative analysis
                                (proportion of species, stoichiometry, affinity,
                                reversibility) of interactions leading to the
                                formation of macromolecular complexes, including
                                protein-protein, DNA-protein, and receptor-ligand
                                interactions [Howlett et al. (2006) Curr. Opin.
                                Chem. Biol. 10:430-436; Lebowitz et al. (2002)
                                Protein Sci. 11:2067-2079; Minton (2000) Exp. Mol.
                                Med. 32:1-5; Rivas et al. (1999) Methods 19:194-212].
                            </p>
                        </div>
                        <div class="col-md-3">
