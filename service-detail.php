                <div class="row">
                    <div class="col-md-12">
                        <ul class="margin-top-2 margin-bottom-2">
                            <li>
                                The Facility at CIB has an analytical XL-I
                                ultracentrifuge equipped with two optical
                                detection systems: UV - VIS variable
                                wavelength and Raleigh interference. T
                                he latter technique enables the study of
                                macromolecules in solutions with
                                high concentration/absorption in the UV – VIS.
                        </li>
                            <li>
                                Since its foundation in 1994, this CIB Facility has
                                given scientific and technical support in the biophysical
                                characterization of proteins and their interactions
                                to a large number of research groups in the CIB, CSIC othe
                                centres, universities and other centres of domestic
                                and foreign research. Moreover, the Facility
                                counts with the advice and collaboration of
                                international experts in the field. 
                                In 2009 this Facility has been granted with the ISO9001
                                certificate in according to the AENOR code ER-0286/2009.
                            </li>
                        </ul>
                        <p>
                            This Facility has also incorporated the technique of dynamic
                            light scattering (DLS) that provide complementary information
                            to analytical ultracentrifugation, with two main advantages,
                            relative to other commonly used analytical methods: low
                            sample volume requirements (15-45 µl) and quick delivery
                            of results (&lt;10 min).
                        </p>
                        <p>
                                DLS allows determining the degree of homogeneity
                                of macromolecular complexes in solution, to measure
                                the translational diffusion coefficient of
                                macromolecules and to calculate their hydrodynamic
                                radius. This instrument (DynaPro MS/X model, Wyatt Inc.)
                                is very well designed to study proteins and macromolecular
                                assemblies (including polymers and liposomes), has a
                                very easy operation (cuvette) and a Peltier temperature
                                regulation system (4-60°C) and can be used to analyze
                                different types of macromolecules such as proteins,
                                polysaccharides and lipid micelles.
                        </p>
                        <ul>
                            <li>
                                We have introduced a static light scattering that can
                                work coupled to a size exclusion chromatography system
                                (SEC-MALS), or as a workstation for the rapid detection
                                and real-time determination of the kinetic and equilibrium
                                properties of biological macromolecules and complexes
                                in solution (CG-SLS). This technique is based upon the
                                methods of static light scattering recently developed by
                                the group of Dr. Allen Minton, NIH [Attri & Minton (2005)
                                Anal. Biochem. 337:103-110].
                                Main module is a multi-angle laser light scattering
                                detector equipped with a flow cell
                                (DAWN EOS model, Wyatt Inc.) that is connected to a
                                refractive index flow detector and to a triple-syringe
                                programmable pump. The information is acquired in minutes
                                (important when the stability of the sample is a factor
                                to consider). Moreover, the system allows determine
                                the association/dissociation kinetics of a given macromolecular
                                reaction (providing that the relaxation time of the
                                reaction is of the order of minutes).
                            </li>
                            <li>
                                Recently we have done in this Facility several
                                studies on the oligomerization and polymerization
                                of proteins by the combination of ultracentrifugation
                                and light scattering techniques (Monterroso et al.
                                (2013) Methods 59: 349–362; del
                                Castillo et al. (2011) Biochemistry, 50: 1991–2003).
                            </li>
                        </ul>
