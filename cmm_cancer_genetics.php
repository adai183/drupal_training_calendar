                        <p>
                            The interest of the group is centered on the biology of adult stem cells (also known as tissue-specific stem cells). A better knowledge of adult stem cell biology will be a requirement for the understanding of the cells that initiate tumors (also known as tumor-initiating cells or cancer stem cells) and the development of cancer.
                        </p>
                        <p>
                            Normal development
                        </p>
                        <p>
                            We have been using three different model systems, mammary gland stem cells, neural stem cells and mesenchymal stem cells. These allow us to answer different questions of adult stem cell biology on each particular model, and in addition, to compare characteristics between them. Of particular interest for us are:
                        </p>
                        <ul>
                            <li>
                                the maintenance of the stem cell characteristics (stemness) in these cells
                            </li>
                            <li>
                                the types of cell divisions adult stem cells undergo in vitro and in vivo, namely asymmetric or symmetric cell divisions (figure 1)
                            </li>
                            <li>
                                their differentiation potential
                            </li>
                        </ul>
                        <p>
                            <img class="img-responsive" src="img/stem.png" alt="" />
                            Figure 1. schematic representation of symmetric and asymmetric cell divisions of the adult stem cells. Adult stem cells can divide either asymmetrically generating a daughter stem cell and another cell able to progress through the differentiation program. With this type of cell division the stem cell number is maintained. Conversely, these cells can divide symmetrically generating two daughter stem cells, increasing the stem cell pool, or generating two daughter cells able to progress through the differentiation program, decreasing the stem cell pool.
                        </p>
                        <p>
                            <strong> Cancer</strong>
                            <br>
                            Although cancer was envisioned for a long time as a group of cells undergoing uncontrolled growth, a large amount of data accumulating during the last 10 years demonstrated that not all the cells within a tumor had the same properties, demonstrating that a only small fraction of cells within the tumor were able to transfer the tumorigenic properties when transplanted into immunocompromised mice recipients, allowing them to grow as xenografts. These cells have been called tumor initiating cells (TIC) or Cancer Stem Cells (CSC) and although it is still a matter of discussion where they come from, it is clear by now that they have characteristics of Stem Cells (SC). More recently, the presence of CSC has been demonstrated in vivo. Data on some models have demonstrated that depletion of CSC on grown up tumors allow shrinkage of the tumor mass, suggesting that cancer therapies targeting the CSC will be more effective than current therapies. Therefore, it is of great interest to improve out knowledge of CSC biology in order to design these therapies.
                        </p>
                        <p>
                            Our group has participated in the development of monoclonal antibodies (mAbs) with therapeutic potential in cancer, one of them targeting neural stem cells and two targeting cancer cells that will generate metastasis in the colon. We keep working in the development of the therapeutic possibilities of these drugs.
                        </p>
