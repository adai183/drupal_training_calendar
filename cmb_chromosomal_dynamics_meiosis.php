                        <p>
                            Control of sister chromatid cohesion/separation is critical to ensure faithful chromosome segregation during mitosis and meiosis. Failures in this mechanism during mitosis often lead to aneuploidy and chromosome instability, a major cause of cancer, while failures during meiosis promote miscarriage, birth defects, and infertility in humans. A key protagonist in this control is the <strong>cohesin</strong> complex, which are composed essentially by four subunits, two of them, called Smc1 and Smc3 (Structural maintenance of chromosomes), are members of a highly conserved protein family also found in prokaryotes and are implicated in various functions related to DNA dynamics, dose compensation, chromosome condensation, recombination, etc. The other two subunits are specific to eukaryotes and, depending on the organism, are termed Scc1/Rad21 or Scc3/STAG, the former mainly for yeast and the latter in higher eukaryotes. In addition to their function in chromatid cohesion during chromosome segregation, our previous results and recent evidences implicate the cohesins in completely different processes, including the regulation of gene expression, DNA repair, and development, suggesting that cohesin functions are more than the glue of chromosomes.
                        </p>
