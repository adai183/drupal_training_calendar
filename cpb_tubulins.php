                    <div class="col-md-12">
                        <p>
                            Proteins from the tubulin superfamily of GTPases are essential to divide cells, to segregate DNA or for cytoskeletal and shape functions. Tubulin-like proteins spread in eukaryotes, bacteria, archaea, plasmids and viruses; they include αβ-tubulin, γ-tubulin, bacterial tubulin BtubA/B, bacterial cell division protein FtsZ, new archael CetZ, plasmid and phage TubZ. They share a few sequence motifs and the same three-dimensional core structure, consisting of an N-terminal GTP binding domain and a GTPase activating domain connected by a central α-helix, but have divergent C-terminal secondary structures and flexible tails.
                        </p>
                        <p>
                            Tubulin-like proteins, except γ-tubulin, typically associate head to tail into polar protofilaments with a 4 nm axial spacing between subunits that form different types of filaments. Their assembly and disassembly is directly linked to their function, forming characteristic subcellular structures such as spindles or division rings, and can produce motility without the assistance of motor proteins. Assembly involves the formation of a subunit-subunit interface where the GTP-binding domain of one subunit in a protofilament interacts with the GTPase activation domain of the next subunit, which complements the GTP pocket and induces GTP hydrolysis. GTP hydrolysis triggers disassembly, which is coupled to free subunits switching into inactive conformation. Polymer dynamics is based on assembly-disassembly events, shows different features depending on the precise mechanism involved, such as dynamic instability or treadmilling, which can produce motility without the assistance of motor proteins.
                        </p>
                        <p>
                            Our work focuses on understanding how these protein assembly machines work, how they evolved, and targeting them with new anticancer and antibacterial molecules; we use biochemical, crystallography, NMR, computational, microbiological, fluorescence, electron microscopy and synthetic approaches at the CIB and collaborating labs.
                        </p>
