                        <p>
                            By using platelets and cell and animal models designed in our laboratory, we investigate the role of platelet and endotelial proteins in the pathophysiology of hemostasis, with the ultimate objective of identifying mechanisms and/or molecules as potential therapeutic targets.
                        </p>
                        <p>
                             Research areas and current objectives:
                             <ul style="list-style: decimal;">
                                 <li>
                                    A first line is intended to clarify the molecular genetic basis and pathogenic mechanisms of hemorrhagic syndromes associated with alterations of surface receptors and other platelet molecules. Currently, we focus mainly on studying the mechanisms involved in platelet dysfunction associated with mutations in the fibrinogen receptor (integrin αIIbβ3) in cases of Glanzmann thrombasthenia and thrombasthenia-like syndromes; analyze the molecular defect and its functional impact on diseases with macrothrombocytopenia such as Bernard-Soulier syndrome and MYH9-related disorders; and identify the responsible gene in familial cases of alteration in the number and size of platelets of unknown cause.
                                 </li>
                                 <li>
                                    In a second line of research we plan to reveal the function, hitherto unknown, of some proteins abundantly expressed on platelets and/or endothelium, through phenotypic characterization of genetically modified mouse lines generated in this laboratory. We have studied the platelets of mice with ablation of the ligand of receptor CD40 (CD40L) at different stages of hematopoietic development, concluding that CD40L fixed to the platelet membrane is involved in the control of hemostasis by acting as a platelet coactivator, but endothelial CD40L has no functional relevance in basal conditions. Moreover, after clarifying the functional significance of the expression of podocalicina (Podxl) in platelets, we have recently discovered that this protein plays an essential role in the control of vascular permeability. Mice lacking Podxl in the endothelium develop a generalized vasculitis with premature death due to organ failure. We intend to clarify the molecular mechanism and validate this animal model for the study of the disease in humans.
                                 </li>
                             </ul>
                        </p>


