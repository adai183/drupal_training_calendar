                        <p>
                            Our scientific <strong>objectives</strong> are related to the use of <strong>microorganisms</strong> (mainly filamentous fungi) and their enzymes in <strong>industrial</strong> processes to obtain fuels, materials and chemicals (White Biotechnology) from <strong>renewable</strong> plant resources. The final aim is to contribute to the sustainable development of our society and reduce the biosphere warming by a reduced consumption of fossil resources. These objectives are well in agreement with those of the new CIB Department of "Environmental Biology".
                        </p>
                        <p>
                            The work of the group has provided important contributions to the knowledge of the <strong>enzymatic system</strong> involved in the <strong>degradation of lignin</strong> (and other recalcitrant compounds) by fungi, which represents a key step for C recycling in land ecosystems and a central issue in the industrial use of plant biomass in agreement with the <strong>Biorefinery concept</strong> (for the integrated production of fuels, chemicals and other products). According to these results, the most recent studies combine basic and more applied aspects (this dual approach in the field of the enzymatic degradation of lignin has been discussed in a review of the group published in Curr Opini Biotechnol, see <a href="http://www.cib.csic.es/repositorio_bd/publicacion/2295/urls_documento/2009-Martinez-COB-20-348-low.pdf" target="blank">Martínez et al. 2008</a>):
                        </p>
                        <ul style="list-style: lower-roman;">
                            <li>
                                <strong>Basic</strong> projects on <strong>structure-function</strong> of key enzymes involved in lignocellulose biodegradation (to <strong>improve</strong> their catalytic properties) including:
                                <ul>
                                    <li>
                                        Ligninolytic hemeperoxidases, like <strong>versatile peroxidase</strong> (VP)
                                    </li>
                                    <li>
                                        Flavooxidases providing peroxide, like <strong>aryl-alcohol oxidase</strong> (AAO)
                                    </li>
                                    <li>
                                        Multicopper oxidases like <strong>laccases</strong> (and their redox mediators)
                                    </li>
                                    <li>
                                        <strong>Esterases</strong> with different substrate specificities
                                    </li>
                                    <li>
                                        Other <strong>hydrolases</strong> of interest, such as cellulases and xylanases
                                    </li>
                                </ul>
                                <img src="img/lignin.png" alt="lignin.png" class="img-responsive margin-top-1" />
                                <i>Lignin oxidation (by LiP or VP) is produced by electron transfer from a surface Trp</i>
                            </li>
                            <li class="margin-top-2">
                                More <strong>applied</strong> projects related to the use of <strong>fungi</strong> and their <strong>enzymes, new</strong> or improved by <strong>rational design</strong> or <strong>directed evolution</strong>, in industrial or environmental applications such as those related to the future <strong>lignocellulose biorefineries</strong> for:
                                <ul>
                                    <li>
                                        Production of second generation <strong>bioethanol</strong>
                                    </li>
                                    <li>
                                        Sustainable production of <strong>materials</strong> and <strong>chemicals</strong>
                                    </li>
                                </ul>
                                <img src="img/ship.png" alt="ship.png" class="img-responsive margin-top-1" />
                                <i>Enzymes can be used as industrial biocatalysts (as an alternative to harsh chemicals) for the development of clean technologies</i>
                            </li>
                        </ul>
                        <p>
                            The different studies performed are illustrated in the sections <a href="#sec3">Publications</a> and <a href="#sec4">Financing</a> of this web site that include the most recent papers (and patents) and the projects in course (together with the information of the last 10 years). A summary of the objectives of these projects (grouped in several lines) is included in the Research projects section.
                        </p>
