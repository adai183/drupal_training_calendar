                        <p>
                            Our Research Group is interested in the study of <strong>Ribosome Biogenesis</strong> in <strong>proliferating plant cells</strong>.
                        </p>
                        <p>
                            The name of the Group is due to the fact that this functional process is visualized in an outstanding domain of the cell nucleus, called <strong>Nucleolus</strong>; furthermore, the structural and morphological aspects that determine the in situ expression of the functional process have a prominent position among our experimental approaches.
                        </p>
                        <p>
                            The <strong>Nucleolus</strong> - its functional organization, its molecular architecture - is probably the best cellular model of a process of <strong>gene expression</strong> in eucaryotes, and its alterations result an excellent indicator of <strong>functional changes</strong> in the cell, especially of those changes related to <strong>cell proliferation</strong> and the <strong>cell division cycle</strong>.
                        </p>
                        <p>
                            These advantageous features have been used to investigate the alterations induced by the <strong>microgravity</strong> environment in the functions of plant cells. Weightlessness, existing on board od spaceships, is an unknown environment for terrestrial living beings, and may induce in them - and specifically in plants - an important stress, against which they develop countermeasures, whose <strong>cellular bases</strong> we are interested in investigating.
                        </p>
