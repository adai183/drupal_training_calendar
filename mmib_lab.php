                        <p>
                            In 1890 von Behring and Kitasato discovered the presence of specific antibodies against the causative agent of tetanus after the acute phase of the disease had passed, and that these antibodies prevented new infections. Since then, protective immune mechanisms have been actively sought to prevent infectious diseases.
                            The action of specific antibodies mainly against bacteria and viruses caused a drastic reduction in epidemic diseases such as diphtheria, whooping cough, tetanus, polio, etc, which had constituted a real problem for public health around the world throughout history. Inactivated microorganisms or purified fragments, as a source of antigens, have provided better protection against many infectious diseases both in man and animals for more than a century. In the last forty years, advances in molecular biology have led to the obtention of effective recombinant vaccines. Furthermore these advances have permitted the study in more depth of the protective mechanisms of the immune system of the host and their relationship with the gene expression of certain genes.
                        </p>
                        <p>
                            Leishmaniasis is a group of parasitic diseases elicited by protozoa of the Leishmania genus from the family Trypanosomatidae. According to the symptoms, leishmaniasis is classified in cutaneous, diffuse cutaneous, mucocutaneous and visceral. The last one generally causes death when untreated, which depends on the immune response of the host and the Leishmania species.Visceral leishmanasis caused by L. infantum is endemic in the Mediterranean basin and the main host is the dog. WHO estimates an incidence of two millions people (five hundred thousand of the visceral type) and a mortality of 60,000 per year for this emergent disease.
                        </p>
                        <p>
                            <strong>Research line I:</strong> Is based in the differential gene expression profiles through transcriptomics and proteomics of  L. infantum biological phases at culture and at the intermediate host, the insect vector. In these studies we have studied in depth the differentiation processes of the parasite: the development of non infective promastigotes to infective forms (metacyclic) and from this one to the intracellular amastigote stage. We have compared the transcriptomes of the main stages by <strong>DNA microarrays</strong> generated in our laboratory from a complete L. infantum shotgun genome library. In addition to the differences found in gene expression between the stages, predominance of down-regulation over up-regulation was observed in amastigotes [1]. Differentially regulated proteins were also detected [2]. In addition, the procyclic (PNA+) and metacyclic (PNA-) subpopulations of cultures in stationary phase were separated by negative selection with peanut agglutinin (PNA) and their transcriptomes were compared by the same methodology [3]. As a result, the up-regulation of genes directly and indirectly related with infectivity (cysteine protease A, proteophosphoglycan polypeptide, etc…). Among others, tyrosine aminotransferase, which appears over-expressed at the metacyclic stage has been characterized and crystallized, in collaboration with the laboratory of  Prof. Peter Myler (SSCGID, Seattle, USA) allowing the pharmacophore determination [4, 5]. Furthermore, it was observed that negative selection with PNA is a marker of metacyclogenesis in L. infantum. In vitro infection assays of U937 human cell line are indicative of higher infectivity of the PNA- subpopulation [3]. We also described factors involved in the differentiation processes, such pH and temperature, which influence the process from promastigote to amastigote stages [6]. Iterative hierarchical clustering indicates that temperature is more influencing than acidification in the differentiation process of promastigotes to amastigotes. We have also compared the differences between L. infantum promastigotes obtained from the anterior gut of Phlebotomus perniciosus (Dr. Ricardo Molina and Dr. Maribel Jiménez, Centro Nacional de Microbiología, Virología e Inmunología Sanitarias, Instituto de Salud Carlos III), the vector responsible for transmission of the disease, and axenically cultured promastigotes, detecting remarkable differences between their transcriptomic profiles [7].
                        </p>
                        <p>
                            A complementary strategy for the study of the genes involved in development as well as with infectivity is the comparison with the processes occurring at related species like Crithidia fasciculata which display a similar differentiation cycle but that remains at the gut of the insect host and do not infect mammalian cells like it happens with Leishmania species. As a result of this experimentation our laboratory has been able to determine a group of genes over or under expressed at the different stages of the parasite cycle related to the infective ability. These studies have been carried out by proteomic approach (2DE-MALDI-TOF/TOF), which allowed us to differentiate the expression profiles of genes related with important functions, like parasite traduction processes and redox homeostasis, between the mobile forms of both parasites [8]. At present, these differences are being assessed by RNAseq and iTRAQ techniques, also including the amastigote stage.
                        </p>
                        <p>
                            The research Line II is based on the study of the immune response against infection applied to the development of vaccines in the dog model. Most of the studies about the immune response against the Leishmania infection have been carried out in the murine model. In this case it has been determined that the protective response is cellular and involves the T lymphocytes. The immune response is dichotomic in mice, in which Th1 response is related to protection and Th2 response with the progression of infection. This is not the case of dogs, that show a balance between both responses.
                        </p>
                        <p>
                            The Molecular Parasitology Laboratory has been working in the development of a DNA vaccine that elicits protection against the experimental infection by L. infantum in dogs. Initially we developed a vaccine based on the gene which encodes for the receptor-like of activated protein kinase C included in two recombinant vehicles for its administration: a pCIneo plasmid (pCI-neo-LACK) and vaccinia virus (VV-LACK) in an homologous and heterologous regime of administration, the latter being more protective. This initial scheme was improved first using a non-replicative vaccinia virus strain (MVA) [9], second by elimination of the antibiotic resistance genes of the pCI-neo plasmid (pORT-LACK) [10]. On the other hand, positive protection results were observed in an evaluation of protection in mice elicited by intranasal delivery of the pCI-neo-LACK/pCI-neo-LACK vaccine in collaboration with Dr. Rossi-Bergman from the Carlos Chagas Institute from Brazil [11].
                        </p>
                        <p>
                            To improve the protection induced so far in dogs of about 70% with a challenge of 108 infective promastigotes, new protective genes are being introduced at the vaccination vehicles which have been also modified to get a better efficiency.
                        </p>
